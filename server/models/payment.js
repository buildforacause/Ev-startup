const Mongoose = require('mongoose');
const { Schema } = Mongoose;

const PaymentSchema = new Schema({
  razorpay_order_id: {
    type: String,
    required: true,
  },
  razorpay_payment_id: {
    type: String,
    required: true,
  },
  razorpay_signature: {
    type: String,
    required: true,
  },
});

module.exports = Mongoose.model('Payment', PaymentSchema);